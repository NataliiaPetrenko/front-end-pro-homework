var num = Number(prompt("Kindly write your number from 1 to 4"));
var result;

switch (num) {
    case 1:
        result = "winter";
        console.log(result);
        break;
    case 2:
        result = "spring";
        console.log(result);
        break;
    case 3:
        result = "summer";
        console.log(result);
        break;
    case 4:
        result = "autumn";
        console.log(result);
        break;
};